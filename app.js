checking_pay.addEventListener('click', () => {

    if (this.transaction_id.value.includes('P') && transaction_id.value.length == 11) {
        var countdownDate = new Date().getTime() + 3 * 60 * 60 * 1000; // 3 soatdan keyingi vaqt

        var countdownFunction = setInterval(function () {
            var now = new Date().getTime();
            var distance = countdownDate - now;

            var hours = Math.floor(distance / (1000 * 60 * 60));
            var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
            var seconds = Math.floor((distance % (1000 * 60)) / 1000);

            h2_2.innerText = (hours + "h " + minutes + "m " + seconds + "s ");

            if (distance < 0) {
                clearInterval(countdownFunction);
                h2_2.innerText = "Countdown finished";
                h2_2.style.color = 'red'
            }
        }, 1000);
        time.style.display = 'block'
    } else {
        transaction_id.style.border = '1px solid red'
        let p_9 = document.getElementById('p-9')
        p_9.style.display = 'block'
    }
})

